export const getWeek = kind => {
    switch (kind) {
        case 1:
            return 'Week 1';
        case 2:
            return 'Week 2';
        case 3:
            return 'Week 3';
        case 4:
            return 'Week 4';
        case 5:
            return 'Week 5';
        default:
            return 'Week 1';
    }
};
